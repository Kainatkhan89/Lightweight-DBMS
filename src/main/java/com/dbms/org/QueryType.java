package com.dbms.org;

/**
 * The QueryType enum represents different types of database queries.
 */
public enum QueryType {
    /**
     * Represents a CREATE query.
     */
    CREATE,

    /**
     * Represents a SELECT query.
     */
    SELECT,

    /**
     * Represents an INSERT query.
     */
    INSERT,

    /**
     * Represents an UPDATE query.
     */
    UPDATE,

    /**
     * Represents a DELETE query.
     */
    DELETE,

    /**
     * Represents a TRANSACTION query.
     */
    TRANSACTION
}
